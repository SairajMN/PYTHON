text = "Python programming"

print("\n Converted string (uppercase):")
print(text.upper())

print("\n Converted string (lowercase):")
print(text.lower())

print("\n Converted string (title case):")
print(text.title())

print("\n Converted string (swapped case):")
print(text.swapcase())

print("\n Converted string (capitalized):")
print(text.capitalize())

print("\n Original string :")
print(text)